    deck_main_total = 0
    deck_side_total = 0    
    # Access the 'cards' dictionary
    cards_dict = deck_info.get("cards", {})
    
    for card_name, versions in cards_dict.items():
        # 'versions' is the list: [{"qty": 4, "code": "..."}]
        for version in versions:

            qty = version.get('qty', 0)
            code = version.get('code', '')
            edition = version.get('edition', 0)

            if version['section'] == 'main':
                deck_main_total += version['qty']
            elif version['section'] == 'sideboard':
                deck_side_total += version['qty']

            
            if ' - ' not in code or ',' in code:
                print(f"❌ CODE ERROR: [{deck_name}] code: '{code}'")

            if not edition:
                print(f"no edition for {card_name} code: {code}")

    
    if deck_main_total != 60 or deck_side_total != 15:
        print(f"{deck_name} -> Main: {deck_main_total}, Side: {deck_side_total}")
    else:
        print(f"{deck_name} OK")
